    private void tabelaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaMouseClicked
        try{
            con=Polaczenie.ConnectDB();
            int row= tabela.getSelectedRow();
            String table_click= tabela.getModel().getValueAt(row, 0).toString();
            String sql= "select * from SZPITAL_DB.ODDZIAL where nazwaoddzialu = '" + table_click + "'";
            pst=con.prepareStatement(sql);
            rs=  pst.executeQuery();
            if(rs.next()){

                String add1=rs.getString("nazwaoddzialu");
                txtNazwa.setText(add1);
                String add2=rs.getString("rodzajoddzialu");
                cmbRodzaj.setSelectedItem(add2);
                int add3 = rs.getInt("liczbalozek");
                String add4= Integer.toString(add3);
                txtLiczba.setText(add4);
                btnNadpisz.setEnabled(true);
                btnUsun.setEnabled(true);
                btnZapisz.setEnabled(false);

            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this,ex);
        }
    }//GEN-LAST:event_tabelaMouseClicked

