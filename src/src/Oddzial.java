    private void btnZapiszActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnZapiszActionPerformed
        try{
            con=Polaczenie.ConnectDB();
            if (txtNazwa.getText().equals("")) {
                JOptionPane.showMessageDialog( this, "Podaj nazwę oddziału","Błąd !", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (cmbRodzaj.getSelectedItem().equals("")) {
                JOptionPane.showMessageDialog( this, "Wybierz rodzaj oddziału","Błąd !", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (txtLiczba.getText().equals("")) {
                JOptionPane.showMessageDialog( this, "Podaj liczbę łóżek","Błąd !", JOptionPane.ERROR_MESSAGE);
                return;
            }
            Statement stmt;
            stmt= con.createStatement();
            String sql1="Select nazwaoddzialu from SZPITAL_DB.oddzial where nazwaoddzialu= '" + txtNazwa.getText() + "'";
            rs=stmt.executeQuery(sql1);
            if(rs.next()){
                JOptionPane.showMessageDialog( this, "Nazwa jest już zajęta","Błąd !", JOptionPane.ERROR_MESSAGE);
                txtNazwa.setText("");
                txtNazwa.requestDefaultFocus();
                return;
            }
          
            String query = "{CALL SZPITAL_DB.zapisz_oddzial(?,?,?)}" ;
            CallableStatement cst = con.prepareCall(query); 
            cst.setString(1,txtNazwa.getText());
            cst.setString(2, (String) cmbRodzaj.getSelectedItem());
            cst.setString(3,txtLiczba.getText());
            cst.execute();
            cst.close();
            
            JOptionPane.showMessageDialog(this,"Zapisano","Potwierdzenie",JOptionPane.INFORMATION_MESSAGE);
            btnZapisz.setEnabled(false);
            Get_Data();
        }catch(HeadlessException | SQLException ex){
            JOptionPane.showMessageDialog(this,ex);
        }
    }//GEN-LAST:event_btnZapiszActionPerformed

